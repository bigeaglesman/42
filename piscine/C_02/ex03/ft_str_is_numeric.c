int	ft_str_is_numeric(char *str)
{
	int	ret;
	int	i;

	i = 0;
	ret = 1;
	while (*(str + i))
	{
		if (!(*(str + i) >= '0' && *(str + i) <= '9'))
			ret = 0;
		i++;
	}
	return (ret);
}
