int	ft_str_is_alpha(char *str)
{
	int	ret;
	int	i;

	i = 0;
	ret = 1;
	while (*(str + i))
	{
		if (!((*(str + i) >= 'A' && *(str + i) <= 'Z')
				|| (*(str + i) >= 'a' && *(str + i) <= 'z')))
			ret = 0;
		i++;
	}
	return (ret);
}
