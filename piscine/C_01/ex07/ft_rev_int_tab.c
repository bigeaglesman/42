void	ft_rev_int_tab(int *tab, int size)
{	
	int	i;
	int	swp;

	i = 0;
	while (i + 1 <= size / 2)
	{
		swp = tab[i];
		tab[i] = tab[size - i - 1];
		tab[size - i - 1] = swp;
		i++;
	}
}
