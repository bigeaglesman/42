int	ft_iterative_factorial(int nb)
{
	int	i;
	int	num;

	i = 1;
	if (nb < 0)
		return (0);
	else if (nb == 0)
		return (1);
	else
	{	
		num = 1;
		while (i <= nb)
		{
			num *= i;
			i++;
		}
		return (num);
	}
}
