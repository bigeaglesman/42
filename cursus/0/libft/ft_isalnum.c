int	ft_isalnum(int c)
{
	int	ret;

	ret = 0;
	if (c >= 65 && c <= 90)
		ret = 1;
	else if (c >= 97 && c <= 122)
		ret = 2;
	else if (c >= 48 && c <= 57)
		ret = 4;
	return (ret);
}
