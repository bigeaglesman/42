int	ft_isalpha(int c)
{
	int	ret;

	ret = 0;
	if (c >= 65 && c <= 90)
		ret = 1;
	else if (c >= 97 && c <= 122)
		ret = 2;
	else
		ret = 0;
	return (ret);
}
